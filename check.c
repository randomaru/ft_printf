#include "ft_printf.h"

int 	is_flags(char *p)
{
	if (*p == '#' || *p == '0' || *p == '+' || *p == '-')
		return (1);
	return (0);
}

int 	is_width(char *p)
{
	if (*p > '0' && *p < '9')
		return (1);
	return (0);
}

